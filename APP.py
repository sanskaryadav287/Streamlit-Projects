def register_page():
    st.title("📝 Create Account")
    st.subheader("Join 10,000+ verified users")

    with st.form("register_form"):
        col1, col2 = st.columns(2)

        with col1:
            name = st.text_input("Full Name *")
            email = st.text_input("Email *")
            phone = st.text_input("Phone Number *", max_chars=10)
            gender = st.selectbox("Gender *", ["Select", "Male", "Female"])

        with col2:
            dob = st.date_input(
                "Date of Birth *",
                min_value=datetime.now() - timedelta(days=35*365),
                max_value=datetime.now() - timedelta(days=21*365)
            )
            password = st.text_input("Password *", type="password")
            confirm_password = st.text_input("Confirm Password *", type="password")
            looking_for = st.selectbox("Looking For", ["Any", "Male", "Female"])

        submitted = st.form_submit_button("🚀 Create Account", type="primary", use_container_width=True)

        if submitted:
            errors = []

            if len(name) < 2:
                errors.append("Name must be at least 2 characters")

            if not validate_email(email):
                errors.append("Invalid email format")

            if not validate_phone(phone):
                errors.append("Phone must be 10 digits (starting with 6-9)")

            if gender == "Select":
                errors.append("Please select gender")

            age_valid, age = validate_age(dob.strftime('%Y-%m-%d'))
            if not age_valid:
                errors.append(f"Age must be 21-35 (your age: {age})")

            if len(password) < 8:
                errors.append("Password must be at least 8 characters")

            if password != confirm_password:
                errors.append("Passwords do not match")

            if errors:
                for error in errors:
                    st.error(f"❌ {error}")
            else:
                st.session_state.user_data = {
                    'name': name,
                    'email': email,
                    'phone': phone,
                    'gender': gender.lower(),
                    'dob': dob.strftime('%Y-%m-%d'),
                    'age': age,
                    'looking_for': looking_for.lower(),
                    'verified': False
                }
                st.session_state.logged_in = True
                st.success("✅ Registration successful! Please verify Aadhaar.")
                st.session_state.current_page = 'verify_aadhaar'
                st.rerun()

