def verify_aadhaar_page():
    st.title("🔐 Aadhaar Verification")
    st.subheader("Required for safety and trust")

    st.info("⚠️ Your Aadhaar number is encrypted and never stored in raw form.")

    if not st.session_state.otp_sent:
        with st.form("aadhaar_form"):
            aadhaar = st.text_input("Aadhaar Number (12 digits) *", max_chars=12)
            send_otp = st.form_submit_button("📱 Send OTP", type="primary", use_container_width=True)

            if send_otp:
                if validate_aadhaar(aadhaar):
                    st.session_state.otp = generate_otp()
                    st.session_state.otp_sent = True
                    st.rerun()
                else:
                    st.error("❌ Please enter valid 12-digit Aadhaar number")
    else:
        st.success(f"✅ OTP sent! (Demo OTP: **{st.session_state.otp}**)")

        with st.form("otp_form"):
            otp_input = st.text_input("Enter 6-digit OTP *", max_chars=6)

            col1, col2 = st.columns(2)
            with col1:
                verify_otp = st.form_submit_button("✅ Verify OTP", type="primary", use_container_width=True)
            with col2:
                resend = st.form_submit_button("🔄 Resend OTP", use_container_width=True)

            if verify_otp:
                if otp_input == st.session_state.otp:
                    st.session_state.verified = True
                    st.session_state.user_data['verified'] = True
                    st.success("✅ Aadhaar verified successfully!")
                    st.session_state.current_page = 'profile_setup'
                    st.rerun()
                else:
                    st.error("❌ Invalid OTP. Please try again.")

            if resend:
                st.session_state.otp = generate_otp()
                st.rerun()

